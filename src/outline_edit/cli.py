DEFAULT_BASE_URL = ""
DEFAULT_TIMEOUT = 30.0
SCHEMA_VERSION = 1
USER_AGENT = f"outline-edit/{__import__('outline_edit').__version__}"
API_VERSION = "2"
MAX_PAGE_SIZE = 100


class KBError(RuntimeError):
    pass


class APIError(KBError):
    pass


@dataclass
class Config:
    base_url: str
    api_key: str | None
    cache_dir: Path
    timeout: float
    env_file: Path


def xdg_config_home() -> Path:
    raw = os.environ.get("XDG_CONFIG_HOME")
    if raw:
        return Path(raw).expanduser()
    return Path.home() / ".config"


def xdg_state_home() -> Path:
    raw = os.environ.get("XDG_STATE_HOME")
    if raw:
        return Path(raw).expanduser()
    return Path.home() / ".local" / "state"


DEFAULT_CONFIG_DIR = xdg_config_home() / "outline-edit"
DEFAULT_STATE_DIR = xdg_state_home() / "outline-edit"
DEFAULT_ENV_FILE = DEFAULT_CONFIG_DIR / "config.env"


def default_cache_dir(base_url: str) -> Path:
    if base_url:
        parsed = urllib.parse.urlparse(base_url)
        host = parsed.netloc or parsed.path or "default"
    else:
        host = "default"
    return DEFAULT_STATE_DIR / "cache" / slugify(host, "default")


def utc_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def file_sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as fh:
        for chunk in iter(lambda: fh.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def slugify(value: str, fallback: str) -> str:
    normalized = unicodedata.normalize("NFKD", value)
    ascii_value = normalized.encode("ascii", "ignore").decode("ascii")
    slug = re.sub(r"[^a-zA-Z0-9]+", "-", ascii_value).strip("-").lower()
    return slug or fallback


def read_env_file(path: Path) -> dict[str, str]:
    values: dict[str, str] = {}
    if not path.exists():
        return values

    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#"):
            continue
        if line.startswith("export "):
            line = line[len("export ") :].strip()
        if "=" not in line:
            raise KBError(f"Invalid env line in {path}: {raw_line!r}")
        key, value = line.split("=", 1)
        key = key.strip()
        value = value.strip().strip("'").strip('"')
        values[key] = value
    return values


def parse_timeout(raw: float | str | None) -> float:
    if raw is None:
        return DEFAULT_TIMEOUT
    try:
        timeout = float(raw)
    except (TypeError, ValueError) as exc:
        raise KBError(f"Invalid timeout value: {raw!r}") from exc
    if timeout <= 0:
        raise KBError(f"Timeout must be positive, got {timeout!r}")
    return timeout


def render_config_template(
    *,
    base_url: str | None = None,
    api_key: str | None = None,
) -> str:
    resolved_base_url = (base_url or "https://your-outline.example.com").rstrip("/")
    resolved_api_key = api_key or "..."
    lines = [
        "# outline-edit configuration",
        "# Generated by `outline-edit init`.",
        "",
        f"OUTLINE_CLI_BASE_URL={resolved_base_url}",
        f"OUTLINE_CLI_API_KEY={resolved_api_key}",
        "",
        "# Optional overrides",
        "# OUTLINE_CLI_CACHE_DIR=/path/to/custom/cache",
        f"# OUTLINE_CLI_TIMEOUT={int(DEFAULT_TIMEOUT)}",
        "",
    ]
    return "\n".join(lines)


def missing_config_error(config: Config) -> KBError:
    missing = []
    if not config.base_url:
        missing.append("OUTLINE_CLI_BASE_URL")
    if not config.api_key:
        missing.append("OUTLINE_CLI_API_KEY")
    missing_fields = ", ".join(missing)
    return KBError(
        "Missing Outline configuration"
        f" ({missing_fields}).\n\n"
        f"Create {config.env_file} with:\n\n"
        "OUTLINE_CLI_BASE_URL=https://your-outline.example.com\n"
        "OUTLINE_CLI_API_KEY=...\n\n"
        "Then run:\n"
        "  outline-edit auth\n\n"
        "Or generate a starter config with:\n"
        "  outline-edit init\n"
        "  outline-edit init --interactive"
    )


def prompt_for_value(label: str, *, default: str | None = None, secret: bool = False) -> str:
    while True:
        suffix = f" [{default}]" if default else ""
        prompt = f"{label}{suffix}: "
        raw = getpass.getpass(prompt) if secret else input(prompt)
        value = raw.strip() or (default or "")
        if value:
            return value
        print("Value required.", file=sys.stderr)


def build_config(args: argparse.Namespace) -> Config:
    file_values = read_env_file(args.env_file) if args.env_file else {}
    env = {**file_values, **os.environ}

    base_url = (args.base_url or env.get("OUTLINE_CLI_BASE_URL") or DEFAULT_BASE_URL).rstrip("/")
    api_key = args.api_key or env.get("OUTLINE_CLI_API_KEY")
    cache_dir_raw = args.cache_dir or env.get("OUTLINE_CLI_CACHE_DIR")
    cache_dir = Path(cache_dir_raw).expanduser() if cache_dir_raw else default_cache_dir(base_url)
    timeout = parse_timeout(args.timeout if args.timeout is not None else env.get("OUTLINE_CLI_TIMEOUT"))

    return Config(
        base_url=base_url,
        api_key=api_key,
        cache_dir=cache_dir,
        timeout=timeout,
        env_file=args.env_file,
    )


class OutlineClient:
    def __init__(self, config: Config):
        if not config.base_url or not config.api_key:
            raise missing_config_error(config)
        self.base_url = config.base_url
        self.api_key = config.api_key
        self.timeout = config.timeout

    def post_json(
        self,
        endpoint: str,
        payload: dict[str, Any] | None = None,
        *,
        accept: str = "application/json",
    ) -> Any:
        body = json.dumps(payload or {}).encode("utf-8")
        request = urllib.request.Request(
            f"{self.base_url}/api/{endpoint}",
            data=body,
            method="POST",
            headers={
                "Authorization": f"Bearer {self.api_key}",
                "Accept": accept,
                "Content-Type": "application/json; charset=utf-8",
                "User-Agent": USER_AGENT,
                "x-api-version": API_VERSION,
            },
        )

        try:
            with urllib.request.urlopen(request, timeout=self.timeout) as response:
                content_type = response.headers.get("Content-Type", "")
                raw = response.read()
        except urllib.error.HTTPError as exc:
            detail = exc.read().decode("utf-8", errors="replace").strip()
            raise APIError(f"{endpoint} failed with HTTP {exc.code}: {detail}") from exc
        except urllib.error.URLError as exc:
            raise APIError(f"{endpoint} failed: {exc.reason}") from exc

        if "application/json" in content_type:
            return json.loads(raw.decode("utf-8"))
        return raw.decode("utf-8")

    def paginate(
        self,
        endpoint: str,
        payload: dict[str, Any] | None = None,
        *,
        limit: int | None = None,
        page_size: int = MAX_PAGE_SIZE,
    ) -> list[dict[str, Any]]:
        if page_size < 1 or page_size > MAX_PAGE_SIZE:
            raise KBError(f"page_size must be between 1 and {MAX_PAGE_SIZE}")

        results: list[dict[str, Any]] = []
        offset = 0

        while True:
            remaining = None if limit is None else max(limit - len(results), 0)
            if remaining == 0:
                break

            current_limit = page_size if remaining is None else min(page_size, remaining)
            request_body = dict(payload or {})
            request_body.update({"offset": offset, "limit": current_limit})
            response = self.post_json(endpoint, request_body)
            batch = response.get("data", [])
            if not isinstance(batch, list):
                raise KBError(f"{endpoint} returned an unexpected payload")

            results.extend(batch)
            if not batch:
                break

            pagination = response.get("pagination") or {}
            total = pagination.get("total")
            offset += len(batch)

            if total is not None and offset >= int(total):
                break
            if len(batch) < current_limit:
                break

        return results


def default_index(base_url: str) -> dict[str, Any]:
    return {
        "schemaVersion": SCHEMA_VERSION,
        "meta": {
            "baseUrl": base_url,
            "updatedAt": None,
        },
        "collections": {},
        "documents": {},
    }


def load_index(cache_dir: Path, base_url: str) -> dict[str, Any]:
    index_path = cache_dir / "index.json"
    if not index_path.exists():
        return default_index(base_url)

    data = json.loads(index_path.read_text(encoding="utf-8"))
    if data.get("schemaVersion") != SCHEMA_VERSION:
        raise KBError(
            f"Unsupported index schema version in {index_path}: {data.get('schemaVersion')}"
        )
    data.setdefault("meta", {})
    data.setdefault("collections", {})
    data.setdefault("documents", {})
    data["meta"]["baseUrl"] = base_url
    return data


def save_index(cache_dir: Path, index: dict[str, Any]) -> None:
    cache_dir.mkdir(parents=True, exist_ok=True)
    index["meta"]["updatedAt"] = utc_now()
    index_path = cache_dir / "index.json"
    index_path.write_text(json.dumps(index, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def slim_user(user: Any) -> dict[str, str] | None:
    if not isinstance(user, dict):
        return None
    return {"id": str(user.get("id") or ""), "name": str(user.get("name") or "")}


def full_url(base_url: str, url_path: str | None) -> str | None:
    if not url_path:
        return None
    if url_path.startswith("http://") or url_path.startswith("https://"):
        return url_path
    return f"{base_url}{url_path}"


def snapshot_path(cache_dir: Path, doc_id: str) -> Path:
    return cache_dir / ".snapshots" / f"{doc_id}.md"


def write_snapshot(cache_dir: Path, doc_id: str, text: str) -> None:
    path = snapshot_path(cache_dir, doc_id)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.rstrip() + "\n", encoding="utf-8")


def read_text_file(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def ensure_clean_snapshot(entry: dict[str, Any], cache_dir: Path) -> bool:
    doc_id = entry.get("id")
    if not doc_id:
        return False
    state = current_state(entry, cache_dir)
    if state["missing"] or state["modified"]:
        return False
    snap = snapshot_path(cache_dir, doc_id)
    if snap.exists():
        return True
    path = state["path"]
    if path is None:
        return False
    write_snapshot(cache_dir, doc_id, read_text_file(path))
    return True


def generate_relative_path(doc_id: str, title: str, collection_name: str | None) -> str:
    collection_slug = slugify(collection_name or "unfiled", "unfiled")
    title_slug = slugify(title, "document")
    filename = f"{title_slug}--{doc_id[:8]}.md"
    return str(Path("collections") / collection_slug / filename)


def current_state(entry: dict[str, Any], cache_dir: Path) -> dict[str, Any]:
    relative_path = entry.get("path")
    sha256 = entry.get("sha256")
    absolute_path = cache_dir / relative_path if relative_path else None

    missing = absolute_path is None or not absolute_path.exists()
    current_hash = None if missing else file_sha256(absolute_path)
    modified = not missing and sha256 is not None and current_hash != sha256
    stale = entry.get("contentRevision") != entry.get("revision")

    return {
        "path": absolute_path,
        "missing": missing,
        "modified": modified,
        "stale": stale,
        "currentHash": current_hash,
    }


def lifecycle_state(entry: dict[str, Any]) -> str:
    if entry.get("deletedAt"):
        return "deleted"
    if entry.get("archivedAt"):
        return "archived"
    if entry.get("publishedAt"):
        return "published"
    return "draft"


def update_collections(
    index: dict[str, Any], collections: list[dict[str, Any]]
) -> dict[str, dict[str, Any]]:
    lookup: dict[str, dict[str, Any]] = {}
    for collection in collections:
        collection_id = collection["id"]
        record = {
            "id": collection_id,
            "name": collection.get("name"),
            "description": collection.get("description"),
            "icon": collection.get("icon"),
            "color": collection.get("color"),
        }
        index["collections"][collection_id] = record
        lookup[collection_id] = record
    return lookup


def match_collection(
    collections: list[dict[str, Any]], selector: str
) -> dict[str, Any]:
    lowered = selector.casefold()
    exact = [
        collection
        for collection in collections
        if collection["id"] == selector or str(collection.get("name", "")).casefold() == lowered
    ]
    if len(exact) == 1:
        return exact[0]
    if len(exact) > 1:
        raise KBError(f"Collection selector {selector!r} is ambiguous")

    fuzzy = [
        collection
        for collection in collections
        if selector in collection["id"] or lowered in str(collection.get("name", "")).casefold()
    ]
    if len(fuzzy) == 1:
        return fuzzy[0]
    if not fuzzy:
        raise KBError(f"No collection matches {selector!r}")
    raise KBError(
        "Collection selector is ambiguous: "
        + ", ".join(f"{item['name']} ({item['id']})" for item in fuzzy[:5])
    )


def fetch_all_collections(client: OutlineClient) -> list[dict[str, Any]]:
    return client.paginate("collections.list")


def get_collection_values(index: dict[str, Any]) -> list[dict[str, Any]]:
    return list(index.get("collections", {}).values())


def resolve_collection_selector(
    index: dict[str, Any], client: OutlineClient, selector: str
) -> dict[str, Any]:
    collections = get_collection_values(index)
    try:
        return match_collection(collections, selector)
    except KBError:
        remote = fetch_all_collections(client)
        update_collections(index, remote)
        return match_collection(remote, selector)


def fetch_documents(
    client: OutlineClient,
    *,
    query: str | None,
    collection_id: str | None,
    document_id: str | None,
    limit: int | None,
    page_size: int,
    status_filter: list[str] | None,
) -> list[dict[str, Any]]:
    if document_id:
        response = client.post_json("documents.info", {"id": document_id})
        document = (response.get("data") or {}).get("document")
        if not document:
            raise KBError(f"documents.info returned no document for {document_id}")
        return [document]

    payload: dict[str, Any] = {}
    if collection_id:
        payload["collectionId"] = collection_id
    if status_filter:
        payload["statusFilter"] = status_filter

    if query:
        payload["query"] = query
        return client.paginate(
            "documents.search_titles",
            payload,
            limit=limit,
            page_size=page_size,
        )

    return client.paginate(
        "documents.list",
        payload,
        limit=limit,
        page_size=page_size,
    )


def export_document_markdown(client: OutlineClient, document_id: str) -> str:
    response = client.post_json("documents.export", {"id": document_id})
    data = response.get("data")
    if not isinstance(data, str):
        raise KBError(f"documents.export returned an unexpected payload for {document_id}")
    return data


def export_revision_markdown(client: OutlineClient, revision_id: str) -> str:
    response = client.post_json("revisions.export", {"id": revision_id})
    data = response.get("data")
    if not isinstance(data, str):
        raise KBError(f"revisions.export returned an unexpected payload for {revision_id}")
    return data


def upsert_document(
    index: dict[str, Any],
    cache_dir: Path,
    base_url: str,
    document: dict[str, Any],
    collection_lookup: dict[str, dict[str, Any]],
    *,
    metadata_only: bool,
    force: bool,
) -> str:
    doc_id = document["id"]
    title = document.get("title") or "Untitled"
    collection_id = document.get("collectionId")
    collection_name = None
    if collection_id:
        collection_name = (collection_lookup.get(collection_id) or {}).get("name")

    existing = index["documents"].get(doc_id, {})
    relative_path = existing.get("path") or generate_relative_path(doc_id, title, collection_name)
    state = current_state(existing, cache_dir) if existing else {
        "path": cache_dir / relative_path,
        "missing": True,
        "modified": False,
        "stale": False,
        "currentHash": None,
    }

    now = utc_now()
    entry = {
        "id": doc_id,
        "title": title,
        "url": full_url(base_url, document.get("url")),
        "urlPath": document.get("url"),
        "urlId": document.get("urlId"),
        "path": relative_path,
        "collectionId": collection_id,
        "collectionName": collection_name,
        "parentDocumentId": document.get("parentDocumentId"),
        "revision": document.get("revision"),
        "contentRevision": existing.get("contentRevision"),
        "createdAt": document.get("createdAt"),
        "updatedAt": document.get("updatedAt"),
        "publishedAt": document.get("publishedAt"),
        "archivedAt": document.get("archivedAt"),
        "deletedAt": document.get("deletedAt"),
        "createdBy": slim_user(document.get("createdBy")),
        "updatedBy": slim_user(document.get("updatedBy")),
        "collaboratorIds": document.get("collaboratorIds") or [],
        "sha256": existing.get("sha256"),
        "metadataSyncedAt": now,
        "contentSyncedAt": existing.get("contentSyncedAt"),
        "lastPullMode": "metadata-only" if metadata_only else "content",
    }

    if metadata_only:
        index["documents"][doc_id] = entry
        return "metadata_only"

    if state["modified"] and not force:
        return "skipped_modified"

    text = document.get("text")
    if not isinstance(text, str):
        raise KBError(
            f"Document {doc_id} did not include markdown text. Check API version handling."
        )

    absolute_path = cache_dir / relative_path
    absolute_path.parent.mkdir(parents=True, exist_ok=True)
    absolute_path.write_text(text.rstrip() + "\n", encoding="utf-8")
    write_snapshot(cache_dir, doc_id, text)
    sha256 = file_sha256(absolute_path)
    entry["sha256"] = sha256
    entry["contentRevision"] = document.get("revision")
    entry["contentSyncedAt"] = now
    index["documents"][doc_id] = entry

    return "updated_existing" if existing else "created"


def resolve_documents_from_index(
    index: dict[str, Any],
    selector: str | None = None,
    *,
    collection: str | None = None,
    modified_only: bool = False,
    stale_only: bool = False,
    missing_only: bool = False,
    cache_dir: Path,
) -> list[dict[str, Any]]:
    documents = list(index["documents"].values())
    if collection:
        lowered = collection.casefold()
        documents = [
            document
            for document in documents
            if lowered in str(document.get("collectionName") or "").casefold()
            or collection in str(document.get("collectionId") or "")
        ]

    if selector:
        lowered = selector.casefold()
        documents = [
            document
            for document in documents
            if selector in document.get("id", "")
            or lowered in str(document.get("title") or "").casefold()
            or lowered in str(document.get("path") or "").casefold()
        ]

    filtered: list[dict[str, Any]] = []
    for document in documents:
        state = current_state(document, cache_dir)
        if modified_only and not state["modified"]:
            continue
        if stale_only and not state["stale"]:
            continue
        if missing_only and not state["missing"]:
            continue
        merged = dict(document)
        merged.update(
            {
                "absolutePath": str(state["path"]) if state["path"] else None,
                "missing": state["missing"],
                "modified": state["modified"],
                "stale": state["stale"],
                "lifecycleState": lifecycle_state(document),
            }
        )
        filtered.append(merged)

    filtered.sort(
        key=lambda item: (
            str(item.get("collectionName") or ""),
            str(item.get("title") or ""),
            str(item.get("id") or ""),
        )
    )
    return filtered


def resolve_single_document(
    index: dict[str, Any], selector: str, *, cache_dir: Path
) -> dict[str, Any]:
    matches = resolve_documents_from_index(index, selector, cache_dir=cache_dir)
    if not matches:
        raise KBError(f"No cached document matches {selector!r}")
    if len(matches) > 1:
        sample = ", ".join(f"{item['title']} ({item['id']})" for item in matches[:5])
        raise KBError(f"Document selector is ambiguous: {sample}")
    return matches[0]


def parse_status_filter(raw: str | None) -> list[str] | None:
    if raw is None:
        return None
    values = [value.strip() for value in raw.split(",") if value.strip()]
    allowed = {"published", "draft", "archived"}
    invalid = [value for value in values if value not in allowed]
    if invalid:
        raise KBError(f"Invalid status values: {', '.join(invalid)}")
    return values or None


def parse_csv(raw: str | None) -> list[str] | None:
    if raw is None:
        return None
    values = [value.strip() for value in raw.split(",") if value.strip()]
    return values or None


def build_unified_diff(
    before: str,
    after: str,
    *,
    fromfile: str,
    tofile: str,
) -> str:
    return "".join(
        difflib.unified_diff(
            before.splitlines(keepends=True),
            after.splitlines(keepends=True),
            fromfile=fromfile,
            tofile=tofile,
        )
    )


def search_index(
    index: dict[str, Any],
    cache_dir: Path,
    query: str,
    *,
    collection: str | None = None,
    title_only: bool = False,
    limit: int | None = None,
) -> list[dict[str, Any]]:
    needle = query.casefold()
    documents = resolve_documents_from_index(
        index,
        collection=collection,
        cache_dir=cache_dir,
    )

    results: list[dict[str, Any]] = []
    for document in documents:
        title = str(document.get("title") or "")
        collection_name = str(document.get("collectionName") or "")
        relative_path = str(document.get("path") or "")

        title_hit = needle in title.casefold()
        collection_hit = needle in collection_name.casefold()
        path_hit = needle in relative_path.casefold()
        line_hits: list[dict[str, Any]] = []

        if not title_only and not document.get("missing"):
            absolute_path = Path(document["absolutePath"])
            for idx, line in enumerate(read_text_file(absolute_path).splitlines(), start=1):
                if needle in line.casefold():
                    line_hits.append({"line": idx, "text": line})
                    if len(line_hits) >= 3:
                        break

        if not (title_hit or collection_hit or path_hit or line_hits):
            continue

        score = 0
        if title_hit:
            score += 4
        if collection_hit:
            score += 2
        if path_hit:
            score += 1
        score += len(line_hits)

        results.append(
            {
                **document,
                "score": score,
                "titleHit": title_hit,
                "collectionHit": collection_hit,
                "pathHit": path_hit,
                "lineHits": line_hits,
            }
        )

    results.sort(
        key=lambda item: (
            -int(item["score"]),
            str(item.get("title") or "").casefold(),
            str(item.get("id") or ""),
        )
    )
    if limit is not None:
        results = results[:limit]
    return results


def update_cached_document_from_remote(
    index: dict[str, Any],
    config: Config,
    document: dict[str, Any],
    *,
    metadata_only: bool,
    force: bool,
) -> dict[str, Any]:
    collection_lookup = index.get("collections", {})
    upsert_document(
        index,
        config.cache_dir,
        config.base_url,
        document,
        collection_lookup,
        metadata_only=metadata_only,
        force=force,
    )
    return index["documents"][document["id"]]


def mark_content_in_sync(
    entry: dict[str, Any], *, update_timestamp: bool = True
) -> dict[str, Any]:
    entry["contentRevision"] = entry.get("revision")
    if update_timestamp:
        entry["contentSyncedAt"] = utc_now()
    return entry


def push_single_document(
    index: dict[str, Any],
    config: Config,
    client: OutlineClient,
    entry: dict[str, Any],
    *,
    publish: bool = False,
    publish_collection_id: str | None = None,
) -> dict[str, Any]:
    state = current_state(entry, config.cache_dir)
    path = state["path"]

    if state["missing"] or path is None:
        return {
            "status": "skipped",
            "reason": "missing",
            "id": entry["id"],
            "title": entry["title"],
        }

    local_text = read_text_file(path)
    remote_response = client.post_json("documents.info", {"id": entry["id"]})
    remote_document = (remote_response.get("data") or {}).get("document")
    if not remote_document:
        raise KBError(f"documents.info returned no document for {entry['id']}")

    update_cached_document_from_remote(
        index,
        config,
        remote_document,
        metadata_only=True,
        force=False,
    )

    if remote_document.get("revision") != entry.get("contentRevision"):
        # Check if this is a metadata-only bump (e.g. publish, archive,
        # restore) where the remote content hasn't actually changed.
        # Compare remote text to the local snapshot — the snapshot records
        # what the content looked like at contentRevision.
        snap = snapshot_path(config.cache_dir, entry["id"])
        remote_text = remote_document.get("text")
        metadata_only_bump = False
        if snap.exists() and isinstance(remote_text, str):
            snapshot_text = read_text_file(snap)
            if remote_text.rstrip() == snapshot_text.rstrip():
                metadata_only_bump = True

        if not metadata_only_bump:
            return {
                "status": "conflict",
                "id": entry["id"],
                "title": entry["title"],
                "localBaseRevision": entry.get("contentRevision"),
                "remoteRevision": remote_document.get("revision"),
                "remoteUpdatedAt": remote_document.get("updatedAt"),
                "remoteUpdatedBy": slim_user(remote_document.get("updatedBy")),
                "url": full_url(config.base_url, remote_document.get("url")),
            }

        # Metadata-only bump: advance contentRevision so the push proceeds
        # against the current remote revision.
        entry["contentRevision"] = remote_document.get("revision")
        refreshed = index["documents"].get(entry["id"])
        if refreshed is not None and refreshed is not entry:
            refreshed["contentRevision"] = remote_document.get("revision")

    if not state["modified"] and not publish:
        return {
            "status": "skipped",
            "reason": "unmodified",
            "id": entry["id"],
            "title": entry["title"],
        }

    payload: dict[str, Any] = {
        "id": entry["id"],
        "text": local_text,
        "editMode": "replace",
    }
    if publish:
        payload["publish"] = True
        if publish_collection_id:
            payload["collectionId"] = publish_collection_id

    update_response = client.post_json("documents.update", payload)
    updated_document = update_response.get("data")
    if not isinstance(updated_document, dict):
        raise KBError(f"documents.update returned an unexpected payload for {entry['id']}")

    pushed_entry = update_cached_document_from_remote(
        index,
        config,
        updated_document,
        metadata_only=False,
        force=True,
    )
    return {
        "status": "updated",
        "id": pushed_entry["id"],
        "title": pushed_entry["title"],
        "revision": pushed_entry.get("revision"),
        "url": pushed_entry.get("url"),
        "path": str(config.cache_dir / pushed_entry["path"]),
        "publishedAt": pushed_entry.get("publishedAt"),
    }


