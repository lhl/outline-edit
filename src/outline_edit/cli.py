def command_restore(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    entry = resolve_single_document(index, args.selector, cache_dir=config.cache_dir)
    state = current_state(entry, config.cache_dir)
    client = OutlineClient(config)

    if args.revision_id and state["modified"] and not args.force:
        raise KBError(
            "Local cached file has unsynced changes; use push, revert the file, or rerun restore with --force."
        )

    payload: dict[str, Any] = {"id": entry["id"]}
    if args.collection:
        collection = resolve_collection_selector(index, client, args.collection)
        payload["collectionId"] = collection["id"]
    if args.revision_id:
        payload["revisionId"] = args.revision_id

    response = client.post_json("documents.restore", payload)
    restored = response.get("data")
    if not isinstance(restored, dict):
        raise KBError("documents.restore returned an unexpected payload")

    refresh_content = bool(args.revision_id or state["missing"])
    refreshed = update_cached_document_from_remote(
        index,
        config,
        restored,
        metadata_only=not refresh_content,
        force=bool(args.force or state["missing"]),
    )
    if not refresh_content:
        refreshed = mark_content_in_sync(refreshed, update_timestamp=not state["modified"])
    save_index(config.cache_dir, index)

    output = {
        "status": "restored",
        "id": refreshed["id"],
        "title": refreshed["title"],
        "url": refreshed.get("url"),
        "revision": refreshed.get("revision"),
        "publishedAt": refreshed.get("publishedAt"),
        "archivedAt": refreshed.get("archivedAt"),
        "deletedAt": refreshed.get("deletedAt"),
        "path": str(config.cache_dir / refreshed["path"]),
        "lifecycleState": lifecycle_state(refreshed),
        "revisionId": args.revision_id,
    }

    if args.json:
        print_json(output)
        return 0

    print(f"restored: {refreshed['title']}")
    print(f"  url: {refreshed.get('url')}")
    print(f"  state: {lifecycle_state(refreshed)}")
    if args.revision_id:
        print(f"  revision: {args.revision_id}")
    return 0


def command_delete(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    entry = resolve_single_document(index, args.selector, cache_dir=config.cache_dir)
    client = OutlineClient(config)

    response = client.post_json(
        "documents.delete",
        {"id": entry["id"], "permanent": args.permanent},
    )
    if not response.get("success"):
        raise KBError("documents.delete returned an unexpected payload")

    now = utc_now()
    output = {
        "status": "permanent_deleted" if args.permanent else "deleted",
        "id": entry["id"],
        "title": entry["title"],
        "url": entry.get("url"),
        "path": str(config.cache_dir / entry["path"]),
        "deletedAt": now,
        "permanent": args.permanent,
    }

    if args.permanent:
        index["documents"].pop(entry["id"], None)
    else:
        deleted_entry = dict(index["documents"].get(entry["id"], entry))
        deleted_entry["deletedAt"] = now
        deleted_entry["metadataSyncedAt"] = now
        index["documents"][entry["id"]] = deleted_entry

    save_index(config.cache_dir, index)

    if args.json:
        print_json(output)
        return 0

    action = "permanently deleted" if args.permanent else "deleted"
    print(f"{action}: {entry['title']}")
    print(f"  url: {entry.get('url')}")
    print(f"  cached_path: {config.cache_dir / entry['path']}")
    return 0


def resolve_revdiff_source(
    client: OutlineClient,
    document: dict[str, Any],
    selector: str,
    recent_revisions: list[dict[str, Any]],
) -> dict[str, Any]:
    lowered = selector.casefold()
    if lowered in {"current", "head"}:
        return {
            "id": None,
            "label": "current",
            "text": export_document_markdown(client, document["id"]),
        }
    if lowered == "latest":
        if not recent_revisions:
            raise KBError(f"No revisions found for {document['title']}")
        revision = recent_revisions[0]
        return {
            "id": revision["id"],
            "label": f"latest:{revision['id']}",
            "text": export_revision_markdown(client, revision["id"]),
        }
    if lowered == "previous":
        if len(recent_revisions) < 2:
            raise KBError(f"No previous revision found for {document['title']}")
        revision = recent_revisions[1]
        return {
            "id": revision["id"],
            "label": f"previous:{revision['id']}",
            "text": export_revision_markdown(client, revision["id"]),
        }
    return {
        "id": selector,
        "label": selector,
        "text": export_revision_markdown(client, selector),
    }


def command_revdiff(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    document = resolve_single_document(index, args.selector, cache_dir=config.cache_dir)
    client = OutlineClient(config)

    needs_recent = any(
        value.casefold() in {"latest", "previous"} for value in (args.from_revision, args.to_revision)
    )
    recent_revisions: list[dict[str, Any]] = []
    if needs_recent:
        recent_revisions = client.paginate(
            "revisions.list",
            {
                "documentId": document["id"],
                "sort": "createdAt",
                "direction": "DESC",
            },
            limit=2,
            page_size=2,
        )

    before = resolve_revdiff_source(client, document, args.from_revision, recent_revisions)
    after = resolve_revdiff_source(client, document, args.to_revision, recent_revisions)
    diff_text = build_unified_diff(
        before["text"],
        after["text"],
        fromfile=f"{document['title']} ({before['label']})",
        tofile=f"{document['title']} ({after['label']})",
    )

    output = {
        "document": {
            "id": document["id"],
            "title": document["title"],
            "url": document.get("url"),
        },
        "from": {
            "selector": args.from_revision,
            "resolvedId": before["id"],
            "label": before["label"],
        },
        "to": {
            "selector": args.to_revision,
            "resolvedId": after["id"],
            "label": after["label"],
        },
        "diff": diff_text,
    }

    if args.json:
        print_json(output)
        return 0

    if not diff_text:
        print(
            f"No revision diff for {document['title']} ({before['label']} -> {after['label']})."
        )
        return 0

    sys.stdout.write(diff_text)
    if not diff_text.endswith("\n"):
        sys.stdout.write("\n")
    return 0


def command_history(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    document = resolve_single_document(index, args.selector, cache_dir=config.cache_dir)
    client = OutlineClient(config)
    revisions = client.paginate(
        "revisions.list",
        {
            "documentId": document["id"],
            "sort": args.sort,
            "direction": args.direction,
        },
        limit=args.limit,
        page_size=min(args.limit or MAX_PAGE_SIZE, MAX_PAGE_SIZE),
    )

    if args.json:
        print_json(
            {
                "document": {
                    "id": document["id"],
                    "title": document["title"],
                    "url": document.get("url"),
                },
                "revisions": revisions,
            }
        )
        return 0

    print(f"{document['title']} revisions:")
    for revision in revisions:
        author = (revision.get("createdBy") or {}).get("name")
        label = f" [{revision['name']}]" if revision.get("name") else ""
        print(f"{revision['id']}  {revision.get('createdAt')}  {author}{label}")
    return 0


def command_log(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    payload: dict[str, Any] = {
        "sort": args.sort,
        "direction": args.direction,
        "auditLog": args.audit,
    }

    if args.selector:
        document = resolve_single_document(index, args.selector, cache_dir=config.cache_dir)
        payload["documentId"] = document["id"]

    if args.collection:
        collection = match_collection(list(index.get("collections", {}).values()), args.collection)
        payload["collectionId"] = collection["id"]

    if args.actor_id:
        payload["actorId"] = args.actor_id

    events = parse_csv(args.events)
    if events:
        payload["events"] = events

    client = OutlineClient(config)
    items = client.paginate(
        "events.list",
        payload,
        limit=args.limit,
        page_size=min(args.limit or MAX_PAGE_SIZE, MAX_PAGE_SIZE),
    )

    if args.json:
        print_json({"events": items})
        return 0

    for event in items:
        actor = (event.get("actor") or {}).get("name")
        print(f"{event.get('createdAt')}  {event.get('name')}  {actor}")
        if event.get("documentId"):
            print(f"  documentId: {event.get('documentId')}")
        if event.get("collectionId"):
            print(f"  collectionId: {event.get('collectionId')}")
    return 0


def command_skill(args: argparse.Namespace, config: Config) -> int:
    skill_file = Path(__file__).with_name("skill.md")
    if not skill_file.exists():
        raise KBError(f"Skill file not found: {skill_file}")
    sys.stdout.write(skill_file.read_text(encoding="utf-8"))
    return 0


