def command_init(args: argparse.Namespace, config: Config) -> int:
    env_file = config.env_file
    existed = env_file.exists()
    if existed and not args.force:
        raise KBError(f"Config file already exists: {env_file}. Use --force to overwrite it.")

    base_url = config.base_url or None
    api_key = config.api_key or None
    if args.interactive:
        if not sys.stdin.isatty() or not sys.stdout.isatty():
            raise KBError("--interactive requires a TTY")
        base_url = prompt_for_value("Outline base URL", default=base_url).rstrip("/")
        api_key = prompt_for_value("Outline API key", default=api_key, secret=True)

    env_file.parent.mkdir(parents=True, exist_ok=True)
    env_file.write_text(
        render_config_template(base_url=base_url, api_key=api_key),
        encoding="utf-8",
    )
    try:
        env_file.chmod(0o600)
    except OSError:
        pass

    payload = {
        "status": "updated" if existed else "created",
        "envFile": str(env_file),
        "interactive": args.interactive,
        "hasBaseUrl": bool(base_url),
        "hasApiKey": bool(api_key),
    }
    if args.json:
        print_json(payload)
        return 0

    action = "Updated" if existed else "Created"
    print(f"{action} config file: {env_file}")
    if args.interactive:
        print("Saved provided Outline credentials.")
    else:
        print("Wrote a starter template. Fill in the required values if needed.")
    print("Next step: outline-edit auth")
    return 0


