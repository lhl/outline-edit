def command_pull(args: argparse.Namespace, config: Config) -> int:
    client = OutlineClient(config)
    index = load_index(config.cache_dir, config.base_url)
    collections = fetch_all_collections(client)
    collection_lookup = update_collections(index, collections)

    collection_id = None
    resolved_collection = None
    if args.collection:
        resolved_collection = match_collection(collections, args.collection)
        collection_id = resolved_collection["id"]

    documents = fetch_documents(
        client,
        query=args.query,
        collection_id=collection_id,
        document_id=args.document_id,
        limit=args.limit,
        page_size=args.page_size,
        status_filter=parse_status_filter(args.status),
    )

    stats = {
        "fetched": len(documents),
        "created": 0,
        "updated_existing": 0,
        "metadata_only": 0,
        "skipped_modified": 0,
    }

    touched: list[dict[str, Any]] = []
    for document in documents:
        result = upsert_document(
            index,
            config.cache_dir,
            config.base_url,
            document,
            collection_lookup,
            metadata_only=args.metadata_only,
            force=args.force,
        )
        stats[result] = stats.get(result, 0) + 1
        if result != "skipped_modified":
            touched.append(index["documents"].get(document["id"], {"id": document["id"]}))

    save_index(config.cache_dir, index)

    payload = {
        "baseUrl": config.base_url,
        "cacheDir": str(config.cache_dir),
        "collection": resolved_collection,
        "query": args.query,
        "documentId": args.document_id,
        "metadataOnly": args.metadata_only,
        "force": args.force,
        "stats": stats,
        "documents": touched,
    }

    if args.json:
        print_json(payload)
        return 0

    print(f"cache_dir: {config.cache_dir}")
    if resolved_collection:
        print(f"collection: {resolved_collection['name']} ({resolved_collection['id']})")
    if args.query:
        print(f"query: {args.query}")
    if args.document_id:
        print(f"document_id: {args.document_id}")
    print(
        "results:"
        f" fetched={stats['fetched']}"
        f" created={stats['created']}"
        f" updated={stats['updated_existing']}"
        f" metadata_only={stats['metadata_only']}"
        f" skipped_modified={stats['skipped_modified']}"
    )
    return 0


