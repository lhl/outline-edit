def command_diff(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    document = resolve_single_document(index, args.selector, cache_dir=config.cache_dir)
    absolute_path = Path(document["absolutePath"])
    if not absolute_path.exists():
        raise KBError(f"Cached file is missing: {absolute_path}")

    current_text = read_text_file(absolute_path)
    basis = "snapshot"
    snap = snapshot_path(config.cache_dir, document["id"])

    if snap.exists():
        baseline_text = read_text_file(snap)
        baseline_name = f"{document['title']} (snapshot)"
    else:
        if ensure_clean_snapshot(document, config.cache_dir):
            baseline_text = read_text_file(snapshot_path(config.cache_dir, document["id"]))
            baseline_name = f"{document['title']} (snapshot)"
        else:
            client = OutlineClient(config)
            baseline_text = export_document_markdown(client, document["id"])
            baseline_name = f"{document['title']} (remote)"
            basis = "remote"

    diff_text = build_unified_diff(
        baseline_text,
        current_text,
        fromfile=baseline_name,
        tofile=f"{document['title']} (local)",
    )

    if args.json:
        print_json(
            {
                "id": document["id"],
                "title": document["title"],
                "basis": basis,
                "path": str(absolute_path),
                "diff": diff_text,
            }
        )
        return 0

    if not diff_text:
        print(f"No diff for {document['title']} ({basis} basis).")
        return 0

    sys.stdout.write(diff_text)
    if not diff_text.endswith("\n"):
        sys.stdout.write("\n")
    return 0


def command_push(args: argparse.Namespace, config: Config) -> int:
    index = load_index(config.cache_dir, config.base_url)
    if args.selector:
        targets = [resolve_single_document(index, args.selector, cache_dir=config.cache_dir)]
    else:
        targets = resolve_documents_from_index(
            index,
            collection=args.collection,
            modified_only=True,
            cache_dir=config.cache_dir,
        )

    if not targets:
        payload = {"pushed": [], "conflicts": [], "skipped": [], "updated": 0}
        if args.json:
            print_json(payload)
        else:
            print("No locally modified cached documents to push.")
        return 0

    client = OutlineClient(config)
    pushed: list[dict[str, Any]] = []
    conflicts: list[dict[str, Any]] = []
    skipped: list[dict[str, Any]] = []

    for document in targets:
        entry = index["documents"].get(document["id"], document)
        if entry.get("contentRevision") is None:
            skipped.append(
                {
                    "id": entry["id"],
                    "title": entry["title"],
                    "reason": "no_local_base",
                }
            )
            continue

        result = push_single_document(index, config, client, entry)
        if result["status"] == "updated":
            pushed.append(result)
        elif result["status"] == "conflict":
            conflicts.append(result)
        else:
            skipped.append(result)

    save_index(config.cache_dir, index)

    payload = {
        "updated": len(pushed),
        "pushed": pushed,
        "conflicts": conflicts,
        "skipped": skipped,
    }

    if args.json:
        print_json(payload)
        return 0

    print(
        f"push results: updated={len(pushed)} conflicts={len(conflicts)} skipped={len(skipped)}"
    )
    for item in pushed:
        print(f"updated: {item['title']} -> rev {item['revision']}")
        print(f"  url: {item['url']}")
    for item in conflicts:
        who = (item.get("remoteUpdatedBy") or {}).get("name")
        print(
            f"conflict: {item['title']} local_base={item['localBaseRevision']} remote={item['remoteRevision']}"
        )
        print(f"  remote_updated: {item['remoteUpdatedAt']} by {who}")
    for item in skipped:
        print(f"skipped: {item['title']} ({item['reason']})")
    return 0


